// Header File
#include <stdio.h>

int main() {
   int a = 10, b = 15;
   
   // Addition
   printf("Addition a+ b = %d\n", a + b);
   
   // Subtraction
   printf("subtrction a - b = %d\n", a - b);
   
   // Multiplication
   printf("multipliction a * b = %d\n", a * b);
   
   // Division
   printf("division a / b = %d\n", a / b);
   
   // Modulus
   printf("modulus a %% b = %d\n", a % b);
   
   // Increment
   a++;
   printf("a after increment = %d\n", a);
   
   // Decrement
   b--;
   printf("b after decrement = %d\n", b);
   
   return 0;
}


